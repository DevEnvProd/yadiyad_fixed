INSERT INTO JobServiceCategory 
(`Name`, `Published`)
SELECT DISTINCT `CategoryName`, 1
FROM `tmpCategoryExpertises` tce
where NOT EXISTS
	(SELECT 0
	FROM JobServiceCategory jsc
	where tce.CategoryName = jsc.Name 
	limit 1)
ORDER BY `CategoryName`;



INSERT INTO Expertise 
(Name, JobServiceCategoryId, IsOthers, Published)
SELECT `ExpertiseName`, jsc.id, 0, 1
FROM `tmpCategoryExpertises` tce
INNER JOIN JobServiceCategory jsc
ON jsc.Name = tce.`CategoryName`
where NOT EXISTS
	(SELECT 0
	FROM Expertise e
	where tce .`ExpertiseName` = e.Name 
	AND e.JobServiceCategoryId  = jsc.Id 
	limit 1)
ORDER BY tce.`CategoryName`, tce.`ExpertiseName`;

